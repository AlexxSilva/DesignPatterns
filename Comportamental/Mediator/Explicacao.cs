using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace DesignPatterns.Comportamental.Mediator
{
    public class Explicacao
    {
        //O padrão Mediator é um padrão comportamental que promove o desacoplamento
        //entre objetos, permitindo que eles se comuniquem indiretamente através de um
        //mediador central. Isso evita dependências diretas entre os componentes e
        //melhora a manutenibilidade do código.

        //Imagine um sistema de chat onde vários usuários podem enviar mensagens.
        //Em vez de cada usuário se comunicar diretamente com os outros, eles passam
        //suas mensagens para um mediador, que gerencia a entrega.

        //Reduz o acoplamento entre objetos, tornando o código mais modular.
        //Facilita a manutenção → Alterações no fluxo de comunicação afetam apenas o mediador.
        //Melhora a escalabilidade → Adicionar novos usuários não impacta os outros diretamente.



    }
}
